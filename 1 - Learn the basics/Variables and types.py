# Python supports two types of numbers: integers and floating point numbers (and other complex types)
myint = 7
print(myint)

# Use one of these notations to define a floating point number
myfloat = 7.0
print(myfloat)
myfloat = float(7)
print(myfloat)

# Strings can be defined using either single o double quotation marks
# Double quotes makes easy include apostrophes on strings
mystring = 'Hello single quote mark'
print(mystring)
mystring = "Hello double quote mark's"
print(mystring)

# Simple operators can be used on numbers and strings
one = 1
two = 2
three = one + two
print(three)

hello = "Hello"
world = "world"
helloworld = hello + ", " + world
print(helloworld)

# Assignments can be done on more than one variable on the same line
a, b = 3, 4
print(a, b)